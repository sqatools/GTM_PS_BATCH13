"""
Properties:

->  Boolean is immutable data type.
->  Boolean contains value as True or False.
->  Boolean is result of any conditional output.

"""

var1 = True
var2 = False
print(var1, type(var1))  # True <class 'bool'>
print(var2, type(var2))  # False <class 'bool'>

print(200 == 100)  # False
print(300 == 300) # True